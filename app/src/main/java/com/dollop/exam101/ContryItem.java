package com.dollop.exam101;

public class ContryItem {

    private String code;
    private int image;

    public ContryItem(String code, int image) {
        this.code = code;
        this.image = image;
    }

    public String getCode()
    {
        return code;
    }
    public int getImage()
    {
        return image;
    }

}
